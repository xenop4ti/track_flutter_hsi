void main() {
  var nilai = 'A';

  switch (nilai) {
    case 'A':
      print("And Lulus dengan sangat baik");
      break;
    case 'B':
      print("And Lulus dengan baik");
      break;
    case 'C':
      print("And Lulus dengan catataan");
      break;
    default:
      print('Anda tidak lulus');
  }
}
